    public void SellsRecord() {

        NewSale ns = new NewSale();
        Connection con;
        PreparedStatement ps;
        
        try 
        {
            String sql = "INSERT INTO sellsRecord (mediId,name,quantity,price,totalBill)" + " VALUES (?,?,?,?,?)";

            con = DriverManager.getConnection("jdbc:mysql://localhost/medical_store", "root", "root");

            Price = Integer.parseInt(mPrice.getText().toString());

            Quantity = Integer.parseInt(buyFld.getText().toString());
            System.out.println("Quantity:" + Quantity);
            TotalBill = Price * Quantity;
            System.out.println("total" + TotalBill);
            mTotalBill.setText(Integer.toString(TotalBill));
            System.out.println("TotalBill:" + mTotalBill);

            ps = con.prepareStatement(sql);
            ps.setString(1, mId.getText());
            ps.setString(2, mName.getText());
            ps.setString(4, mPrice.getText());
            ps.setString(3, buyFld.getText());
            ps.setString(5, mTotalBill.getText());

            ///////////////Price Count///////////////////////////////
            int save = ps.executeUpdate();

            System.out.println("executeQuery");

            if (save > 0)
            {
                System.out.println("Inserted");
                JOptionPane.showMessageDialog(null, "Sold");
                ns.updateTable();
                dispose();
            } 
            else 
            {
                System.out.println("Not Inserted");
                JOptionPane.showMessageDialog(null, "Not Sold");
            }

        } 
        catch (SQLException ex) 
        {

            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);

        }
    }

