    private void sellBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sellBtnActionPerformed

        Medicines medicine = new Medicines();

        NewSale ns = new NewSale();
        Connection con;
        PreparedStatement ps;

        try 
        {
            buy = Integer.parseInt(buyFld.getText().toString());

        } 
        catch (Exception x) 
        {
            JOptionPane.showMessageDialog(null, "Quantity Empty");
            ns.updateTable();
        }

        System.out.println("Buy:" + buy);
        int up = stock - buy;
        String sql = "UPDATE medicines SET current_stock='" + up + "'WHERE id='" + mId.getText() + "'";
        
        if (stock < buy) 
        {
            JOptionPane.showMessageDialog(null, "Medicine Out Of Stock");
        } 
        else 
        {

            try 
            {

                con = DriverManager.getConnection("jdbc:mysql://localhost/medical_store", "root", "");
                ps = con.prepareStatement(sql);
                int save = ps.executeUpdate();

                System.out.println("executeQuery");

                if (save > 0) {
                    System.out.println("Inserted");
                    ns.updateTable();
                    medicine.updateTable();
                    ns.setVisible(true);
                    dispose();
                    SellsRecord();

                } else {
                    System.out.println("Not Inserted");
                    JOptionPane.showMessageDialog(null, "Not saved");

                }

            } 
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, "Insert Valid Data");
                dispose();
            }
        }


    }//GEN-LAST:event_sellBtnActionPerformed

